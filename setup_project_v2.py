with open('src/report_generator.py', 'w', encoding='utf-8') as f:
    f.write('from datetime import datetime\n')
    f.write('from pathlib import Path\n')
    f.write('\n')
    f.write('def generate_text_report(df, result, station="Synthetic", output_dir="results"):\n')
    f.write('    sep = "=" * 72\n')
    f.write('    lines = [sep,\n')
    f.write('        "GNSS SIGNAL QUALITY MONITORING REPORT",\n')
    f.write('        "Galileo / GPS Multi-Constellation Performance Analysis",\n')
    f.write('        "Station     : " + station,\n')
    f.write('        "Generated   : " + datetime.utcnow().strftime("%Y-%m-%d %H:%M:%S UTC"),\n')
    f.write('        "Window      : " + str(df["epoch"].min()) + " to " + str(df["epoch"].max()),\n')
    f.write('        sep, "",\n')
    f.write('        "[1] SERVICE AVAILABILITY (>= 4 valid satellites)"]\n')
    f.write('    for const, avail in result.availability_pct.items():\n')
    f.write('        s = "OK" if avail >= 99 else ("WARN" if avail >= 95 else "CRIT")\n')
    f.write('        lines.append("  " + const.ljust(10) + str(avail).rjust(7) + "%   [" + s + "]")\n')
    f.write('    lines += ["", "[2] SIGNAL QUALITY - C/N0 Statistics"]\n')
    f.write('    lines.append(result.cn0_stats.to_string())\n')
    f.write('    n = len(result.anomalies)\n')
    f.write('    lines += ["", "[3] ANOMALIES DETECTED (threshold: 32 dB-Hz)",\n')
    f.write('              "  Total events: " + str(n)]\n')
    f.write('    if n > 0:\n')
    f.write('        lines.append("  Affected SVs: " + ", ".join(sorted(result.anomalies["sv"].unique())))\n')
    f.write('    lines += ["", "[4] SIGNAL CONTINUITY",\n')
    f.write('              "  Tracking gaps: " + str(len(result.gaps))]\n')
    f.write('    lines += ["", "[5] MULTI-SYSTEM COMPARISON",\n')
    f.write('              "  Galileo - GPS C/N0 delta: " + str(result.galileo_gps_delta) + " dB-Hz"]\n')
    f.write('    lines += ["", "[6] SUMMARY",\n')
    f.write('              "  Galileo Service Status: " + result.summary_status, "", sep]\n')
    f.write('    report = "\\n".join(lines)\n')
    f.write('    Path(output_dir).mkdir(exist_ok=True)\n')
    f.write('    with open(output_dir + "/monitoring_report.txt", "w") as f:\n')
    f.write('        f.write(report)\n')
    f.write('    print(report)\n')

