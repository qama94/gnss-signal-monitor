with open('src/signal_monitor.py', 'w', encoding='utf-8') as f:
    f.write('import numpy as np\n')
    f.write('import pandas as pd\n')
    f.write('from dataclasses import dataclass\n')
    f.write('\n')
    f.write('CN0_THRESHOLD = 32.0\n')
    f.write('MIN_SVS = 4\n')
    f.write('GAP_S = 90\n')
    f.write('\n')
    f.write('@dataclass\n')
    f.write('class MonitoringResult:\n')
    f.write('    availability_pct: dict\n')
    f.write('    cn0_stats: pd.DataFrame\n')
    f.write('    anomalies: pd.DataFrame\n')
    f.write('    gaps: pd.DataFrame\n')
    f.write('    epoch_stats: pd.DataFrame\n')
    f.write('    summary_status: str\n')
    f.write('    galileo_gps_delta: float\n')
    f.write('\n')
    f.write('def compute_availability(df):\n')
    f.write('    total = df["epoch"].nunique()\n')
    f.write('    result = {}\n')
    f.write('    for const in df["constellation"].unique():\n')
    f.write('        valid = df[(df["constellation"] == const) & df["valid"]]\n')
    f.write('        per_epoch = valid.groupby("epoch")["sv"].nunique()\n')
    f.write('        available = (per_epoch >= MIN_SVS).sum()\n')
    f.write('        result[const] = round(100.0 * available / total, 2)\n')
    f.write('    return result\n')
    f.write('\n')
    f.write('def compute_cn0_stats(df):\n')
    f.write('    valid = df[df["valid"]].copy()\n')
    f.write('    valid["elev_band"] = pd.cut(valid["elevation"], bins=[0, 20, 40, 90],\n')
    f.write('                               labels=["Low (0-20deg)", "Mid (20-40deg)", "High (>40deg)"])\n')
    f.write('    return valid.groupby(["constellation", "elev_band"])["cn0"].agg(\n')
    f.write('        count="count", mean="mean", std="std",\n')
    f.write('        p5=lambda x: x.quantile(0.05)).round(2)\n')
    f.write('\n')
    f.write('def detect_anomalies(df):\n')
    f.write('    valid = df[df["valid"]].copy()\n')
    f.write('    anom = valid[valid["cn0"] < CN0_THRESHOLD].copy()\n')
    f.write('    anom["type"] = "Low C/N0"\n')
    f.write('    anom["severity"] = np.where(anom["cn0"] < 28, "SEVERE", "MODERATE")\n')
    f.write('    return anom.reset_index(drop=True)\n')
    f.write('\n')
    f.write('def detect_tracking_gaps(df):\n')
    f.write('    gaps = []\n')
    f.write('    for sv, grp in df[df["valid"]].groupby("sv"):\n')
    f.write('        grp = grp.sort_values("epoch")\n')
    f.write('        diffs = grp["epoch"].diff().dt.total_seconds()\n')
    f.write('        mask = diffs > GAP_S\n')
    f.write('        for epoch, gap_s in zip(grp["epoch"][mask], diffs[mask]):\n')
    f.write('            gaps.append({"sv": sv, "epoch": epoch, "gap_s": round(gap_s, 0),\n')
    f.write('                         "constellation": grp["constellation"].iloc[0]})\n')
    f.write('    return pd.DataFrame(gaps) if gaps else pd.DataFrame(\n')
    f.write('        columns=["sv", "epoch", "gap_s", "constellation"])\n')
    f.write('\n')
    f.write('def compute_epoch_stats(df):\n')
    f.write('    return df[df["valid"]].groupby(["epoch", "constellation"]).agg(\n')
    f.write('        sv_count=("sv", "nunique"),\n')
    f.write('        mean_cn0=("cn0", "mean"),\n')
    f.write('        min_elev=("elevation", "min")).reset_index()\n')
    f.write('\n')
    f.write('def run_monitoring(df):\n')
    f.write('    availability = compute_availability(df)\n')
    f.write('    cn0_stats = compute_cn0_stats(df)\n')
    f.write('    anomalies = detect_anomalies(df)\n')
    f.write('    gaps = detect_tracking_gaps(df)\n')
    f.write('    epoch_stats = compute_epoch_stats(df)\n')
    f.write('    gal = availability.get("Galileo", 0)\n')
    f.write('    status = "NOMINAL" if gal >= 99 else ("DEGRADED" if gal >= 95 else "CRITICAL")\n')
    f.write('    valid = df[df["valid"]]\n')
    f.write('    delta = round(valid[valid["constellation"]=="Galileo"]["cn0"].mean() -\n')
    f.write('                  valid[valid["constellation"]=="GPS"]["cn0"].mean(), 2)\n')
    f.write('    return MonitoringResult(availability_pct=availability, cn0_stats=cn0_stats,\n')
    f.write('        anomalies=anomalies, gaps=gaps, epoch_stats=epoch_stats,\n')
    f.write('        summary_status=status, galileo_gps_delta=delta)\n')

with open('src/data_generator.py', 'w', encoding='utf-8') as f:
    f.write('import numpy as np\n')
    f.write('import pandas as pd\n')
    f.write('from datetime import datetime, timedelta\n')
    f.write('\n')
    f.write('GALILEO_SVS = ["E01","E02","E03","E04","E05","E07","E08","E09",\n')
    f.write('               "E11","E12","E14","E18","E19","E21","E24","E25",\n')
    f.write('               "E26","E27","E30","E31","E33","E36"]\n')
    f.write('GPS_SVS = ["G01","G03","G06","G07","G09","G11","G14","G17",\n')
    f.write('           "G19","G21","G22","G25","G28","G30","G31","G32"]\n')
    f.write('\n')
    f.write('def generate(duration_hours=6, sampling_rate_s=30, inject_events=True, seed=42):\n')
    f.write('    np.random.seed(seed)\n')
    f.write('    n = int(duration_hours * 3600 / sampling_rate_s)\n')
    f.write('    t0 = datetime(2026, 4, 27, 8, 0, 0)\n')
    f.write('    epochs = [t0 + timedelta(seconds=i * sampling_rate_s) for i in range(n)]\n')
    f.write('    records = []\n')
    f.write('    for sv in GALILEO_SVS + GPS_SVS:\n')
    f.write('        is_gal = sv.startswith("E")\n')
    f.write('        const = "Galileo" if is_gal else "GPS"\n')
    f.write('        base = 44.5 if is_gal else 42.5\n')
    f.write('        ps = np.random.randint(0, n // 3)\n')
    f.write('        pe = min(ps + np.random.randint(n // 3, int(n * 0.9)), n)\n')
    f.write('        sz = pe - ps\n')
    f.write('        me = np.random.uniform(25, 85)\n')
    f.write('        t = np.linspace(0, 3.14159, sz)\n')
    f.write('        elevs = np.maximum(5.0, me * np.sin(t) + np.random.normal(0, 0.3, sz))\n')
    f.write('        azims = np.linspace(np.random.uniform(0, 180), np.random.uniform(180, 360), sz)\n')
    f.write('        for j, i in enumerate(range(ps, pe)):\n')
    f.write('            elev = elevs[j]\n')
    f.write('            cn0 = base + 0.32 * elev + np.random.normal(0, 1.8)\n')
    f.write('            if inject_events:\n')
    f.write('                if 100 <= i <= 104 and sv in ["E01","E03","G07"]:\n')
    f.write('                    cn0 -= np.random.uniform(6, 12)\n')
    f.write('                if elev < 20 and np.random.random() < 0.08:\n')
    f.write('                    cn0 -= np.random.uniform(2, 7)\n')
    f.write('                if 300 <= i <= 315:\n')
    f.write('                    cn0 += np.random.normal(-1.5, 2.0)\n')
    f.write('            cn0 = max(18.0, round(cn0, 2))\n')
    f.write('            records.append({"epoch": epochs[i], "sv": sv, "constellation": const,\n')
    f.write('                "cn0": cn0, "elevation": round(elev, 2),\n')
    f.write('                "azimuth": round(azims[j] % 360, 1), "valid": elev >= 10 and cn0 >= 30})\n')
    f.write('    return pd.DataFrame(records)\n')

